// Regenerates supabase/seed.sql from lib/constants.js.
// Run with: node scripts/generate-seed-sql.mjs
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";
import { BRANDS, ACCOUNTS, RAW_STORES, parseStores } from "../lib/constants.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

function sqlStr(v) {
  if (v === null || v === undefined) return "null";
  return `'${String(v).replace(/'/g, "''")}'`;
}

const lines = [];
lines.push("-- Auto-generated by scripts/generate-seed-sql.mjs. Do not edit by hand.");
lines.push("-- Run once against a fresh Supabase project after schema.sql.");
lines.push("");

lines.push("insert into brands (id, name) values");
lines.push(
  BRANDS.map((b) => `  (${sqlStr(b.id)}, ${sqlStr(b.name)})`).join(",\n") +
    "\non conflict (id) do update set name = excluded.name;"
);
lines.push("");

lines.push("insert into accounts (code, name, type, category, group_label, sort_order) values");
lines.push(
  ACCOUNTS.map(
    (a, i) =>
      `  (${sqlStr(a.code)}, ${sqlStr(a.name)}, ${sqlStr(a.type)}, ${sqlStr(a.category)}, ${sqlStr(a.group)}, ${i})`
  ).join(",\n") +
    "\non conflict (code) do update set name = excluded.name, type = excluded.type, category = excluded.category, group_label = excluded.group_label, sort_order = excluded.sort_order;"
);
lines.push("");

const stores = parseStores(RAW_STORES);
lines.push("insert into stores (code, name, brand_id, complex_type, store_type) values");
lines.push(
  stores
    .map(
      (s) =>
        `  (${sqlStr(s.code)}, ${sqlStr(s.name)}, ${sqlStr(s.brandId)}, ${sqlStr(s.complexType)}, ${sqlStr(s.storeType)})`
    )
    .join(",\n") + "\non conflict (code) do nothing;"
);
lines.push("");

const outPath = path.join(__dirname, "..", "supabase", "seed.sql");
writeFileSync(outPath, lines.join("\n"), "utf8");
console.log(`Wrote ${outPath} (${BRANDS.length} brands, ${ACCOUNTS.length} accounts, ${stores.length} stores)`);
