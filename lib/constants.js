export function parseStores(raw) {
  return raw
    .trim()
    .split("\n")
    .map((line) => {
      const [name, code, brand, complexType, storeType] = line.split(",").map((s) => s.trim());
      const brandId = BRANDS.find((b) => b.name === brand)?.id || BRANDS[0].id;
      return { name, code, brandId, complexType, storeType };
    });
}

